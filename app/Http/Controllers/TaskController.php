    public function change_status(Task $task)
    {
        $max_id = DB::table('statuses')->max('id');

        if($task->status_id < $max_id)
        {
            $task->status_id += 1;
            $task->save();
        }
        else 
        {
            $task->status_id = 1;
            $task->save();
        }

        return redirect()->back();
    }

